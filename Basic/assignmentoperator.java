// Java code to illustrate how assignment operator works

public class assignmentoperator {

              // main method

              public static void main(String[] args) {

                            // initializing variables

                            int a = 40;

                            // Displaying variable

                            System.out.println("a = " + a);

                            // a = a + 5

                            a += 5;
                            System.out.println("After += " + a);

                            // a = a - 5

                            a -= 2;
                            System.out.println("After -= " + a);

                            // a = a * 5
                            
                            a *= 3;
                            System.out.println("After *= " + a);

                            // a = a / 4
                            a /= 4;
                            System.out.println("After /= " + a);

                            // a = a % 5
                            
                            a *= 3;
                            System.out.println("After %= " + a);


              }
              
}
